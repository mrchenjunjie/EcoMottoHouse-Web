import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.ResponseBody;

@Controller
public class LogoutWebQueryController {
        //@Autowired
        //private IPersonService personService;
	@RequestMapping(value={"/api/web-logout"}, method=RequestMethod.POST)
    public String onSubmit(HttpServletRequest request, HttpServletResponse response) throws JSONException, IOException, InterruptedException {
		
		response.setContentType("text/html");
        Cookie[] cookies = request.getCookies();
        if(cookies != null){
        for(Cookie cookie : cookies){
            if(cookie.getName().equals("JSESSIONID")){
                System.out.println("JSESSIONID="+cookie.getValue());
                break;
            }
        }
        }
        //invalidate the session if exists
        HttpSession session = request.getSession(false);
        System.out.println("User="+session.getAttribute("user"));
        if(session != null){
            session.invalidate();
        }
        return "login";
           
	}
}
